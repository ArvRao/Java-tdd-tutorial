public class Age {
    public static void main(String[] args) {
        // System.out.println("This is from Ages class");
        int age = 24;
        switch (age) {
            case 19:
                System.out.println("This is the time to get serious");
            case 20:
                System.out.println("Starting point!");
                break;
            case 21:
                System.out.println("Work now!");
                break;
            case 22:
                System.out.println("Make a good network");
                break;
            case 23:
                System.out.println("Time to shine!");
                break;
            default:
                System.out.println("Keep exploring!");
                break;
        }
    }


}
